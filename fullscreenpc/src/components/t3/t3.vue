                this.alarminfo = p[$(e.target).attr('item')] ? p[$(e.target).attr('item')][0] : [];
                this.alarmflg = true;
            } else {
                this.alarmflg = false;
                var $g = $(e.target).parent().parent();
                //($g.attr("item"))
                if ($g.attr("item")) {
                    $g.css('cursor', 'pointer')
                    var left = $g.offset().left - 100;
                    var top = $g.offset().top - 50;
                    $("body").append('<div class="ccc" style="left:' + left + 'px;top:' + top + 'px;position: absolute;border:1px solid rgba(0,168,255,1);padding:10px;background:rgba(0,168,255,.3);color:#fff;border-radius:5px;">' + $g.attr("item") + '</div>')
                } else {
                    $(".ccc").remove();
                }
            }
        },
        mousewheel(e) {
            if (e.deltaY < 0) {
                if (this.scalenum < 3) {
                    this.scalenum = this.scalenum + 0.1;
                    fixedx = fixedx - 45;
                    fixedy = fixedy - 29;
                }
            } else {
                if (this.scalenum > 0.5) {
                    this.scalenum = this.scalenum - 0.1;
                    fixedx = fixedx + 45;
                    fixedy = fixedy + 29;
                }
            }
        },
        mousedown(e) {
            this.nowx = $("#svgid").offset().left.toFixed(0) * 1;
            this.nowy = $("#svgid").offset().top.toFixed(0) * 1;
            this.xp = e.clientX - this.nowx;
            this.yp = e.clientY - this.nowy;
            this.ondown = true;
        },
        mouseup() {
            this.ondown = false;
        },
        mousemove(e) {
            if (this.ondown) {
                var leftnum = e.clientX - this.xp - fixedx;
                var topnum = e.clientY - this.yp - fixedy;
                this.leftnum = leftnum.toFixed(0) * 1;
                this.topnum = topnum.toFixed(0) * 1;
            }
        },
        showdailog() {
            //(1)
        }
    },
    data() {
        return {
            scalenum: 1,
            leftnum: 0,
            topnum: 0,
            xp: 0,
            yp: 0,
            nowx: 0,
            nowy: 0,
            ondown: false,
            alarmflg: false,
            alarminfo: [],
        }
    }
}
